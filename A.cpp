{
	int N, P;
	scanf("%d", &N);
	for(int i=0;i<N;i++){
		scanf("%d", &P);
		printf("k");
		for(int j=0;j<P;j++){
			printf("a");
		}
		for(int j=0;j<2;j++){
			printf("m");
			for(int k=0;k<P;k++){
				printf("e");
			}
			printf("h");
			for(int k=0;k<P;k++){
				printf("a");
			}
		}
		printf("\n");
	}
	return 0;
}
